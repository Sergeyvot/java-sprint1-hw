public class Converter {

    public static double convertSteps (int sumSteps) { // Метод возвращает пройденные шаги в км
        double mileage = sumSteps * 0.00075;
        return mileage;
    }

    public static double countingCalories (int sumSteps) { // Метод возвращает потраченные ккал

        double numberCalories = sumSteps * 0.05;
        return numberCalories;
    }
}
